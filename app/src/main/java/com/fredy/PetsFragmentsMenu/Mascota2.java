package com.fredy.PetsFragmentsMenu;

public class Mascota2 {

    private String likes;
    private int foto;

    public Mascota2(int foto,  String likes) {

        this.likes = likes;
        this.foto = foto;

    }

    public String getLikes() {
        return likes;
    }

    public void setLikes(String likes) {
        this.likes = likes;
    }

    public int getFoto() {
        return foto;
    }

    public void setFoto(int foto) {
        this.foto = foto;
    }




}
